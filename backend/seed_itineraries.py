def insert_itinerary(db, data, user_id):
    itin = Itinerary(
        title=data["title"],
        destination=', '.join(
            p.strip() for p in data["destination"].split(',') if p.strip()
        ),
        description=None,
        start_date=data["start_date"],
        end_date=data["end_date"],
        estimated_budget=float(data["estimated_budget"]),
        currency=data["currency"],
        status=data["status"],
        is_public=data["is_public"],
        user_id=user_id,
    )
    db.add(itin)
    db.flush()

    for tag in data.get("tags", []):
        db.add(ItineraryTag(tag=tag, itinerary_id=itin.id))

    for day_data in data["days"]:
        day = ItineraryDay(
            itinerary_id=itin.id,
            day_number=day_data["day_number"],
            date=day_data["date"],
            title=f"Day {day_data['day_number']}",
            estimated_cost=0,
        )
        db.add(day)
        db.flush()

        for order, d in enumerate(day_data.get("destinations", [])):
            db.add(Activity(
                day_id=day.id,
                title=d["title"],
                description=d.get("description"),
                location=d["location"],
                formatted_address=d.get("formatted_address"),
                latitude=d.get("latitude"),
                longitude=d.get("longitude"),
                place_id=d.get("place_id"),
                place_types=d.get("place_types"),
                rating=d.get("rating"),
                start_time=d.get("start_time"),
                end_time=None,
                activity_type=d.get("activity_type", "destination"),
                cost=d.get("cost", 0.0),
                priority="medium",
                is_completed=False,
                display_order=order,
            ))

    db.commit()
    print(f"  ✓  {data['title']}")


