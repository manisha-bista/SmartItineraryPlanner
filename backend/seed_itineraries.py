def dest(title, location, description=None, time_val=None, cost=0.0,
         activity_type="destination",
         lat=None, lng=None, place_id=None, address=None,
         place_types=None, rating=None):
    return dict(
        title=title,
        location=location,
        description=description,
        formatted_address=address,
        latitude=lat,
        longitude=lng,
        place_id=place_id,
        place_types=place_types,
        rating=rating,
        start_time=time_val,
        end_time=None,
        activity_type=activity_type,
        cost=float(cost),
        priority="medium",
        is_completed=False,
    )


